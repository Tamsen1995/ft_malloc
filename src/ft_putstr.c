#include "../includes/ft_malloc.h"

static void			my_putchar(char c)
{
	write(1, &c, 1);
}

void				ft_putstr(char const *s)
{
	int i;

	i = 0;
	while (s[i] != '\0')
	{
		my_putchar(s[i]);
		i++;
	}
}
